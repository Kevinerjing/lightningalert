function inferUploadId(item) {
  const candidates = [];

  if (typeof item?.resourceLink === "string") {
    candidates.push(item.resourceLink);
  }
  if (typeof item?.imagePath === "string") {
    candidates.push(item.imagePath);
  }
  if (Array.isArray(item?.resources)) {
    item.resources.forEach((entry) => {
      if (entry && typeof entry === "object" && typeof entry.url === "string") {
        candidates.push(entry.url);
      }
    });
  }

  const matched = candidates.find((value) => value.includes("uploads/"));
  if (!matched) {
    return null;
  }

  const normalizedPath = matched.replace(/^(\.\.\/)+/, "");
  return stableId("upload", normalizedPath);
}

function normalizeSubjectSlug(subject) {
  const normalized = String(subject || "").trim().toLowerCase();
  if (normalized === "science") {
    return "science";
  }
  if (normalized === "math") {
    return "math";
  }
  if (normalized === "english") {
    return "english";
  }
  return "science";
}

function normalizePriority(priority) {
  const normalized = String(priority || "").trim().toLowerCase();
  if (normalized === "high") {
    return "High";
  }
  if (normalized === "low") {
    return "Low";
  }
  return "Medium";
}

function normalizeTaskSource(source) {
  const normalized = String(source || "").trim().toLowerCase();
  if (normalized === "classroom-sync") {
    return "classroom_sync";
  }
  if (normalized === "codex-chat-upload") {
    return "chat_upload";
  }
  return "manual";
}

function normalizeMistakeSource(source) {
  const normalized = String(source || "").trim().toLowerCase();
  if (normalized === "codex-chat-upload") {
    return "chat_upload";
  }
  return "manual";
}

function normalizeUploadCategory(category) {
  const normalized = String(category || "").trim().toLowerCase();
  if (["docs", "mistakes", "timetable"].includes(normalized)) {
    return normalized;
  }
  return "other";
}

function insert(tableName, record) {
  const columns = Object.keys(record);
  const values = columns.map((column) => sqlValue(record[column]));
  statements.push(`INSERT INTO ${tableName} (${columns.join(", ")}) VALUES (${values.join(", ")});`);
}

function sqlValue(value) {
  if (value === null || value === undefined) {
    return "NULL";
  }
  if (typeof value === "number") {
    return Number.isFinite(value) ? String(value) : "NULL";
  }
  return `'${String(value).replaceAll("'", "''")}'`;
}

function stableId(prefix, input) {
  const digest = crypto.createHash("sha1").update(`${prefix}:${input}`).digest("hex").slice(0, 16);
  return `${prefix}-${digest}`;
}

function serializeItem(item) {
  if (item && typeof item === "object") {
    return JSON.stringify(item);
  }
  return String(item);
}

function nowIso() {
  return new Date().toISOString();
}

async function readJson(filePath) {
  const raw = await fs.readFile(filePath, "utf8");
  return JSON.parse(raw);
}
